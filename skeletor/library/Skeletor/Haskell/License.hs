-- | Inverts 'licenseId'

readSpdxLicenseIdentifier :: String -> Maybe SpdxLicenseIdentifier
readSpdxLicenseIdentifier s = Map.lookup s table
  where

  table = Map.fromList entries

  entries = entry <$> allLicenses

  entry license = (licenseId license, license)

